def get_all_resources():
    """Get a flat list of all resources with download URLs."""
    resources = []

    for category, items in BIBLEPROJECT_RESOURCES.items():
        for item in items:
            title = item["title"]
            slug = item["slug"]

            # Add transcript if available
            if item.get("has_transcript", False):
                resources.append(
                    {
                        "category": category,
                        "title": title,
                        "type": "Transcript",
                        "url": f"{TRANSCRIPT_BASE}{slug}/",
                        "slug": slug,
                    }
                )

            # Add study notes if available
            if item.get("has_study_notes", False):
                resources.append(
                    {
                        "category": category,
                        "title": title,
                        "type": "Study-Notes",
                        "url": f"{STUDY_NOTES_BASE}{slug}/",
                        "slug": slug,
                    }
                )

    return resources


def get_resources_by_category(category_name):
    """Get resources for a specific category."""
    if category_name not in BIBLEPROJECT_RESOURCES:
        return []

    resources = []
    for item in BIBLEPROJECT_RESOURCES[category_name]:
        title = item["title"]
        slug = item["slug"]

        if item.get("has_transcript", False):
            resources.append(
                {
                    "category": category_name,
                    "title": title,
                    "type": "Transcript",
                    "url": f"{TRANSCRIPT_BASE}{slug}/",
                    "slug": slug,
                }
            )

        if item.get("has_study_notes", False):
            resources.append(
                {
                    "category": category_name,
                    "title": title,
                    "type": "Study-Notes",
                    "url": f"{STUDY_NOTES_BASE}{slug}/",
                    "slug": slug,
                }
            )

    return resources


def get_statistics():
    """Get statistics about available resources."""
    total_items = sum(len(items) for items in BIBLEPROJECT_RESOURCES.values())
    total_transcripts = sum(
        1
        for items in BIBLEPROJECT_RESOURCES.values()
        for item in items
        if item.get("has_transcript", False)
    )
    total_study_notes = sum(
        1
        for items in BIBLEPROJECT_RESOURCES.values()
        for item in items
        if item.get("has_study_notes", False)
    )

    return {
        "total_categories": len(BIBLEPROJECT_RESOURCES),
        "total_items": total_items,
        "total_transcripts": total_transcripts,
        "total_study_notes": total_study_notes,
        "total_resources": total_transcripts + total_study_notes,
    }


